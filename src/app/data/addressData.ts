// All entries flat for search/lookup by id
export const ALL_ENTRIES: Record<string, AddressEntry> = {}

Object.values(INSTITUTION_CATEGORIES).flat().forEach(e => { ALL_ENTRIES[e.id] = e })
PERSONS.forEach(e => { ALL_ENTRIES[e.id] = e })

// Group persons by first letter of last name
export function getPersonsByLetter(): Record<string, AddressEntry[]> {
  const result: Record<string, AddressEntry[]> = {}
  PERSONS.forEach(p => {
    const letter = p.name[0].toUpperCase()
    if (!result[letter]) result[letter] = []
    result[letter].push(p)
  })
  return result
}
